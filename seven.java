public class seven {
    public static void main(String[] args) {
              int r=5,c=1,d=1;
              for(int i=r;i>=0;i--){
                  for(int j=0;j<c+i;j++){
                      System.out.print(" ");
                  }
                  for(int k=0;k<d;k++){
                      System.out.print("*");
                      
                  }
                  System.out.println("");
                  d++;
              }
            }
            
        }
        
    
